function calculateFive(optionPrice) {
    var rawAnswer = optionPrice * 1.05;
    roundedPercent = Math.round(rawAnswer * 100)/100
    if (isNaN(roundedPercent)) {
      return ("Please input numbers.");
    } else if (roundedPercent === 0) {
      return ("Not possible");
    } else if (roundedPercent > 0) {
      return (roundedPercent);
    } else if (roundedPercent < 0) {
      return (roundedPercent);
    } else {
      return ("An error occured, please try again.");
    }
  }

